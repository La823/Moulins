export default function AdminLayout({ children }) {
  const { user, logout } = useAuth();
  const pathname = usePathname();

  const isActive = (href) => {
    if (href === "/admin") return pathname === "/admin";
    return pathname.startsWith(href);
  };

  const isAdmin = user?.role === "admin";
  const userPermissions = user?.permissions || [];

  const visibleNavItems = NAV_ITEMS.filter((item) => {
    // Admin-only items: only visible to admins
    if (item.adminOnly) return isAdmin;
    // Employee-only items: only visible to employees
    if (item.employeeOnly) return !isAdmin;
    // Permission-gated items: admins see all, employees need the permission
    if (item.permission) {
      return isAdmin || userPermissions.includes(item.permission);
    }
    // No restriction (e.g. Dashboard)
    return true;
  });

  const displayName = user?.username || user?.phone_number || "";
  const roleLabel = isAdmin ? "Admin" : "Staff";

  return (
    <AuthGuard allowedRoles={["admin", "employee"]}>
      <div className="min-h-screen bg-gray-50 flex">
        {/* Sidebar */}
        <aside className="w-60 bg-gray-900 text-white flex flex-col fixed inset-y-0 left-0 z-30">
          <div className="px-5 py-5 border-b border-gray-800">
            <Link href="/admin" className="flex items-center gap-2">
              <Image
                src="/Moulins Logo High Res - V2.png"
                alt="Moulins"
                width={100}
                height={32}
                className="h-7 w-auto brightness-0 invert"
              />
              <span className="text-xs text-gray-400 font-medium uppercase tracking-wider">
                {roleLabel}
              </span>
            </Link>
          </div>

          <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
            {visibleNavItems.map((item) => {
              const active = isActive(item.href);
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                    active
                      ? "bg-white/10 text-white"
                      : "text-gray-400 hover:text-white hover:bg-white/5"
                  }`}
                >
                  {item.icon}
                  {item.label}
                </Link>
              );
            })}
          </nav>

          <div className="px-3 py-4 border-t border-gray-800 space-y-3">
            <div className="px-3">
              <p className="text-sm text-gray-300 font-medium truncate">
                {displayName}
              </p>
              <p className="text-xs text-gray-500 truncate">
                {user?.phone_number}
              </p>
            </div>
            <button
              onClick={logout}
              className="w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium text-gray-400 hover:text-white hover:bg-white/5 transition-colors"
            >
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
              </svg>
              Logout
            </button>
          </div>
        </aside>

        {/* Main content */}
        <div className="flex-1 ml-60">
          <main className="p-6">{children}</main>
        </div>
      </div>
    </AuthGuard>
  );
}
